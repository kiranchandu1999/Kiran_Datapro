class Literals {
    public static void main(String[] args) {
        // Literals
        int n1 = 10;
        long n2 = 25L;
        float n3 = 16.86f;
        double n4 = 5.86;
        char ch1 = 'x';
        System.out.println(n1);
        System.out.println(n2);

        // unicode system
        System.out.println("\u0041"); // A
    }
}