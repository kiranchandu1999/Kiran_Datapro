// Data types
// Number
// define variable using lower_snake_case
let a = 15
console.log(a);
// always define constant variable using UPPER_SNAKE_CASE
const PI = 3.14;
console.log(PI);
// print data type given variable
console.log(typeof a); // Number
console.log(typeof PI); // Number


// string
let s1 = "25";
console.log(s1);
// print data type of s1
console.log(typeof s1); // string

// we can define strings using single quote(''), double quote("") and backtick(``)
let str1 = `Our country name is "India", Indian's prime minister is Modi`;
console.log(str1);

let institute = "Datapro";
let branch = "Dwarakanagar";
let city = "vizag";

console.log("I'm learning javascript in " + institute + " of " + branch + " branch in " + city);
console.log(`I'm learning javascript in ${institute} of ${branch} branch in ${city}`);


// boolean (true / false)
let b1 = true;
console.log(b1);
// print type of b1
console.log(typeof b1);


// undefined
let u1;
console.log(u1); // undefined
console.log(typeof u1);


// null
let n1 = null;
console.log(n1); // null
// data type of n1
console.log(n1);