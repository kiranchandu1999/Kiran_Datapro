class UnicodeExample {
    public static void main(String[] args) {
        // literals
        int a = 400;
        long b = 500L;

        float f = 12.68F;
        double d = 5.69;

        System.out.println(a);
        // unicode system
        System.out.println("\u0041");
        System.out.println("\u0928\u092E\u0938\u0924\u0947");
    }
}