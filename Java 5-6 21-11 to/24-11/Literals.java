public class Literals {
    public static void main(String[] args) {
        // Literals
        int n1 = 15;
        long n2 = 20L;
        char ch = 'a';
        System.out.println(n1);

        // Unicode system
        System.out.println("\u0041"); // A

        // Binary literals
        int b1 = 0b101;
        System.out.println(b1); // 5

        // Octal
        int o1 = 056;
        System.out.println(o1); // 46

        // Hexa deciaml
        int h1 = 0x2f;
        System.out.println(h1); // 47
    }
}
