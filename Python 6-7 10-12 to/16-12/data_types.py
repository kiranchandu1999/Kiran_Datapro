# The following are variables and data types
a = 10  # int variable
b = 15.36  # float variable
c = "Datapro" # string variable
d = True # boolean variable
e = None # None variable
f = "3.14" # string variable

print("Data types")
# type() function defines the datatype of a variable
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))

# id() function will give memory location of the variable data
print(id(a))
print(id(b))
print(id(c))
print(id(d))
print(id(e))
print(id(f))



