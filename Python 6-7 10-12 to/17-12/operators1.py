# Operators examples
num1 = int(input("Enter number 1:"))
num2 = int(input("Enter number 2:"))

# Arithmetic operators
print("Arithmetic operators")
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 // num2) # floor divison
print(num1 % num2) # reminder
print(num1 ** num2) # exponentiation


