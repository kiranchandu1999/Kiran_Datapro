public class Operators {
    public static void main(String[] args) {

        System.out.println((6>5) && false);
        System.out.println(5 ^ 1); // 101 & 001
        // 101  ---> 1010
        System.out.println(5 << 1);

        // System.out.println(args[1]);

        int a = 5;

        System.out.println(a++);

        System.out.println(a);
    }
}