
public class Operators {
    public static void main(String[] args) {
        // variable declaration
        int num1 = 10;
        int num2 = 5;

        // add two numbers
        System.out.println(num1 + num2);
        // sub
        System.out.println(num1 - num2);
        // mul
        System.out.println(num1 * num2);
        // div
        System.out.println(num1 / num2);

        System.out.println(3 ^ 5);
    }
}
